# arrey
tommaso <- c(10, 20, 30, 50, 70) # this is an arrey
erika <- c(1, 3, 5, 6, 7, 9)

# function(): plot()
plot(tommaso, erika)
plot(tommaso, erika, pch=19) # pch is changing the pointing characters

plot(tommaso, erika, pch=19, col="blue") # changing the color


plot(tommaso, erika, pch=19, col="blue", cex=5) # character exaggeration

plot(tommaso, erika, pch=19, col="blue", cex=5, xlab="cannabis", ylab="dolphins")
